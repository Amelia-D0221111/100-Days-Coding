public class Main {
    public static void main(String[] args) {

        // Konversi String ke int
        String strInt = "123";
        int intValue = Integer.parseInt(strInt);
        System.out.println("String to int: " + intValue);

        // Konversi Double ke double
        Double doubleObj = 3.14159;
        double doubleValue = doubleObj; 
        System.out.println("Double to double: " + doubleValue);

        // Konversi Character ke char
        Character charObj = 'A';
        char charValue = charObj; 
        System.out.println("Character to char: " + charValue);

        // Konversi Boolean ke boolean
        Boolean boolObj = true;
        boolean boolValue = boolObj; 
        System.out.println("Boolean to boolean: " + boolValue);
    }
}
